/* 7. Calcular el doble y el triple
• Solicita un número.
• Muestra su doble y su triple. */


// Solicitar número al usuario
let numero = Number(prompt("Ingresa un número:"));

// Calcular doble y triple
let doble = numero * 2;
let triple = numero * 3;

// Mostrar resultado
alert("El doble es: " + doble);
alert("El triple es: " + triple);